package com.calculator;

public class Calc {
	
	static public int adder (int a, int b)
	   {
	      return a + b;
	   }
	
	static public int multiple (int a, int b)
	   {
	      return a * b;
	   }
	static public int subtract (int a, int b)
	   {
	      return a - b;
	   }
	static public float dvide (int a, int b)
	   {
	      return a / b;
	   }
}
